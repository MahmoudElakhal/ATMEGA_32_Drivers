#include "../../STD_TYPES.h"
#include "../../BIT_MATH.h"
#include "../../MCAL/DIO/DIO_Interface.h"
#include "LED_Interface.h"


void LED_ON( u8 Copy_u8PORT_ID , u8 Copy_u8Pin_ID )
{
	DIO_u8SetPinValue(Copy_u8PORT_ID , Copy_u8Pin_ID , DIO_u8PIN_HIGH);
}
void LED_OFF(u8 Copy_u8PORT_ID , u8 Copy_u8Pin_ID )
{
	DIO_u8SetPinValue(Copy_u8PORT_ID , Copy_u8Pin_ID , DIO_u8PIN_LOW);
}

